# most basic way of printing numbers
print(2)

# using the BODMAS order of operations
print(2 + 3 * 4)  # Output: 14

# using the modulus operator
print(10 % 3)  # Output: 1  

# don't forget to use str() function to convert numbers to strings
print("The result is: " + str(2 + 3 * 4))

# using the pow() function
print(pow(2,2)) # Output: 4

# using the min() and mox() functions
print(min(2, 3))  # Output: 2
print(max(2, 3))  # Output: 3

# using the round() function
print(round(2.6))  # Output: 3